<h3>Speech of Loremaster Marco Hall</h3>

<p><font color='#788a68'>„Wolves! German trothkin and most honored guests! Today Clan Wolf stands united to celebrate two of our mightiest warriors! Two warriors who have served the Clan for more than a decade! Wolves by heart and soul! 
They are the epitome of leadership! They gave council, when needed! They communicated and argued with other factions hour after hour. In times of separation and betrayal, they kept our Clan together, by cutting off the infected flesh. Again and again they led our touman into battle. Under their leadership we conquered parts of the Inner Sphere more than once. They showed us our ultimate goal and keep guiding us on our way to Terra. 
Doing so, they strengthened our will and set our focus into the one possible direction. To keep true to the traditions of the founder and to honor the rules and trials carved into stone by him. Even in times of personal loss, they were here for us! They made more of us than only a Clan amongst many. They forged the bonds of friendship between strangers. Their unbreakable will made us not only comrades. Neg, they made a Wolf pack of us. Wolves incarnate! To bestow them with our greatest honor, allowing Khan Merlin Conners to grand them their Bloodright, by vouching for them is the least I could do. Wolves! I was there the whole time and followed in their wake. To honor the new bearers of the Bloodnames Ward is the least we can do!
Glory to Khan Meldric Ward! 
Glory to Khan Michael 'Riot' Ward! 
Glory to Clan Wolf! 
Seyla!”</font></p>

<h3>Speech of Khan Meldric Ward</h3>

<p><font color='#788a68'>„I would like to say something to anyone of you being new to German Clan Wolf and to you, our honorable guests from Clan Wolf International who may not know us for too long. For the last decade and more, Riot and me, as well as Nimrod have been loyal to the Wolf. We have been giving what was needed and sometimes more than that to bring our beloved clan forward into a glorious future. We had our own profile and many times people from other factions got confronted with our strong and uncompromising attitude towards the traditions of our founder. We had to learn how to deal with harmfull individuals, we had to learn to be strict at one hand and flexible at the other, to listen to the warriors and officers under our command as well as to guide them. We learned how to position ourselves and the clan towards other factions in the battle for Terra and all of the Inner Sphere. Learning from our mistakes, we were hard when it was necessary, flexible when it was needed and always full of dedication. Our weapons were words as well as silence, dedication as well as trust, politics as well as BattleMechs. While I can just say this, it may be testified by others. We had hard times, we had victorious times. Always at least one of us has been actively pushing the limits. For the same long time we were stuck in the situation of carrying inappropriate ranks and no bloodnames where a bloodname would have been necessary by definition. We are true to our traditions and all three of us felt it to be wrong to bestow a bloodname onto ourselves. Thus we kept on going with the ranks we gained in our trials of position - and without bloodnames. Knowing we were out of sync with the tradition, for a long time we let non-player characters represent the Keshik to reflect the situation, but people happened to not getting the point of this and became very confused about the different identities. While the process of MWO and finally CW the non player characters were dropped by Riot and me, while Nimrod kept his Loremaster char. The situation felt uncomfortable to everyone as we knew it was a bit off. Still, putting a bloodname or even a rank onto each other or even to one self was no valid option to anyone of us. That is the reason why I held the rank as a StarCommander until last week. Today you, Khan Merlin Conners, the known and respected leader of Clan Wolf International corrected this, by your own initiative and intention. We are proud the work and dedication has been seen! We take this greatest honor for a clan warrior without humbleness, but with pride and dignity, because we know it is right. I, now speaking only for my person, will continue to serve the clan with all my power and I will do so in close cooperation with my Keshik, the warriors of German Clan Wolf, the international council and with you, Khan Merlin Conners, who I see as my equal in everything from now on until the vision of Kerensky was fullfilled and beyond. Seyla!”</font></p>

