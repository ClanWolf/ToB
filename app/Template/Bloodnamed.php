<p>Clan Wolf Germany has a conclave of all warriors, celebrating and discussing the promotion of the newly SCols Meldric and Riot. Everyone is concentrated on the matters at hand. Suddenly Khan Merlin Conners steps into the room, followed by two of his bloodnamed warriors and members of the Clan Wolf International Keshik! All Clan Wolf warriors turn their faces to the arrivers, alarmed and battle ready, but deeply surprised. Why are these mighty warriors here? What in Kerensky‘s name has happened? All these questions were visible to a well trained eye. With a smile on his face Loremaster Marco Hall awaits the great opening of what is coming next.</p>

<h3>Bloodright ceremony of Khan Meldric Ward</h3>

<p>
<font color='#00b1ba'><i>Khan Merlin Conners (CWI):</i></font><br>
<font color='#788a68'>„Warriors of Clan Wolf hear my Voice! I, Merlin Conners, Khan of Clan Wolf International wish to address this conclave.”</font>
</p>
 
<p>
<font color='#00b1ba'><i>Loremaster Marco Hall (CWG):</i></font><br>
<font color='#788a68'>„Well met, Khan Conners! The conclave is yours!”</font>
</p>

<p>
<font color='#00b1ba'><i>Khan Merlin Conners (CWI):</i></font><br>
<font color='#788a68'>„For many years now we have enjoyed the camaraderie of our German Brothers. We have fought together, we have cried together but most of all we have shared the bond of being wolves together. However something has been bothering me recently and I felt it was time to do something about it. At the heart of every clan is its leadership. Many people have asked me what it takes to be a leader. A clear vision with the ability to paint a picture of the Future, a strong Mission statement they can articulate and put into action, interpersonal Skills allowing them to communicate with people on a level they can understand and, finally, a can-do attitude. Which, when added all together, gives someone the ability to inspire.<br><br>
I personally have seen all of these in Meldric and Riot over the last 2 years and I have documented evidence of their actions in doing the right thing for this unit, doing the right thing for the community but most of all doing the right thing for CLAN WOLF for many years before that. These are the facts and beyond reproach. I have on a number of occasions been invited, and have attended German Clan Wolf Events, even being honored by being allowed to attend some of your most intimate rituals. The honor and respect I have been shown by all warriors come from the steadfast example put forward by Meldric and Riot. I look at Meldric as my equal in everything. However this is not the documented case. Meldric, you have proved yourself to have the heart, soul and body of a true warrior, nae Khan of this Clan  and Riot you are his strong right hand who has stood with him through the good and the bad. This has not gone unnoticed and this is why I interrupt this conclave to put things right. Normally a warrior would fight for his place in the eugenics program which ensures that only the strongest warriors leave their legacy. Is there anyone amongst you who feels that Meldric and Riot and not the strongest amongst you who hold this Clan together. They fight on a daily basis to better this Clan and have been recognised by those who have gone before and worthy addition to the programme.”</font>
</p>

