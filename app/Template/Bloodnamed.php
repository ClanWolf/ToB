<p>Khan Merlin Conners was unfolding the silk house Wards ceremonial wrappings from the mask to reveal the true face of Meldric Ward. He stoped to look closely at the wolf mask before him. Turning to Meldric he handed him his mask that until this moment he himself had not seen. Taking a moment he looks at it with proud eyes, than turns before the assembled Wolves who all ware there true faces.</p>

<p>
<font color='#00b1ba'><i>Khan Meldric Ward (CWG):</i></font><br>
<font color='#788a68'>„I am Meldric Ward! Know me now and forever by my true face!”</font>
</p>

<p>Lifting the mask before him for all too clearly see the black face of the wolf, snarling and showing his gleaming white teeth. Its matted fur spikes sharply shine like obsidian shards, the green star of our father stand proud in the centre of it forehead and the red lined eyes stare intently into the heart of its enemies. The mask in enhanced with chainmail to act as protection with the glyphs hanging from the bottom signifying the proud fighting heritage of it wearer. This is the mask of a warrior of skill and cunning, all who see it bear witness, lowering the mask to his face.</p>
 
